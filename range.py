# The range() function is to generate arithmetic progression
# NOTE : the number given as end point will never b part of the numbers generated so, numbers generated
#        will always be n - 1

for i in range(10) :
    print(i); # 0 - 9


for e in range(1, 90) :
    print(e); # 1 - 89

print(list(range(5, 10))); # Gives a list with numbers in it from 5 - 9